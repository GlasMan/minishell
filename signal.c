#include "minishell.h"
#include <readline/readline.h>
#include <readline/history.h>

void	ft_sig(int signo)
{
	if (signo == SIGINT)
	{
		printf("\n");
		rl_on_new_line();
		rl_replace_line("", 0);
		rl_redisplay();
	}
}


void	ft_signal(void)
{
	signal(SIGINT, ft_sig);
}
